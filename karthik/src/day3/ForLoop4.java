package day3;

public class ForLoop4 {

	public static void main(String[] args) {
		// to repeat the task
		/*i=1 => initial value
		 * i<=3 => condition checking
		 * i++ => i=i+1
		 */
		byte i;
		for (i=3;i>=1;i++)
		{
		 System.out.println(i+" "+"karthik"); 
		}
		
		System.out.println("see u"+i);
		 
	}

}
