package day2;

public class OperatoresExample {

	public static void main(String[] args) {
	 int a=10, b=20;
	 int c;
	 
	 c=a+b;// 12+3 => 15
	 System.out.println("the c is: "+c);
	 c=a-b; // 12-3 => 9
	 System.out.println("the c is: "+c);
	 c=a*b; // 12-3 => 9
	 System.out.println("the c is: "+c);
	 c=a/b; // 12-3 => 9
	 System.out.println("the c is: "+c);
	 c=a%b; // 12-3 => 9
	 System.out.println("the c is: "+c);
	 
	 
	 
	 
	 
	}

}
